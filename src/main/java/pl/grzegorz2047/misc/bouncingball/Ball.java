package pl.grzegorz2047.misc.bouncingball;

public class Ball {
    public int x, y;
    public int speedX, speedY;
    public int radius;

    public Ball(int x, int y, int speedX, int speedY, int radius) {
        this.x = x;
        this.y = y;
        this.speedX = speedX;
        this.speedY = speedY;
        this.radius = radius;
    }
}
